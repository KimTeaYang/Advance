    private void pay_bt_payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_bt_payActionPerformed
    	int con = JOptionPane.showConfirmDialog(this, "결제 하시겠습니까?");// Y:0,N:1,C:2
		if (con == 0) {
			if (pay_chk_card.isSelected() && seat_Table.getRowCount() > 0) {
				int idx = user.get(0).getIdx_pk();
				int pay_price = 0;
				for (SeatVO pvo : seat) {
					pay_price += pvo.getSeat_price_fk2();
				}
				int n = playDAO.pay_insert(idx, pay_price);

				payInfo.add(playDAO.pay_select(idx).get(0));
				for (SeatVO svo : seat) {
					ticketInfo.add(playDAO.ticket_select(svo.getPlay_number_fk2(), svo.getSeat_number_fk2()).get(0));
				}

				// payInfo table 생성
				for (TicketVO tvo : ticketInfo) {
					playDAO.payinfo_insert(payInfo.get(0).getPay_number_pk(), tvo.getTicket_number_pk());
				}
				
				pay_chk_card.setSelected(false);
				seat.clear();
				seat_Table.setModel(new DefaultTableModel());
				showMsg(pay_price+"원이 결제되었습니다.");
			}else {
				showMsg("예매 좌석이 없거나 결제 방식이 체크되지 않았습니다.");
			}
		}else{
			showMsg("결제를 취소하셨습니다.");
		}
    }//GEN-LAST:event_pay_bt_payActionPerformed

