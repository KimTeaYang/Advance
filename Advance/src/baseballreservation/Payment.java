    // 좌석 선택
    private void seat(String index) {
    	if(index.equals("선택")) {
    		showMsg("좌석을 선택해주세요.");
    	}else {
    		int seatnum = Integer.parseInt(index);
        	int playnum = Integer.parseInt(pay_lb_playnum.getText());
        	seatinfo = playDAO.seat_select(seatnum,playnum);
        	
        	pay_lb_seat.setText(seatinfo.get(0).getSeat_grade_fk2()+"등급 "+
        						seatinfo.get(0).getSeat_number_fk2()+"번 좌석");
        	pay_lb_price.setText(seatinfo.get(0).getSeat_price_fk2()+"원");
        	
        	pay_chk_red.setSelected(false);
        	pay_chk_sky.setSelected(false);
        	pay_chk_purple.setSelected(false);
        	pay_chk_navy.setSelected(false);
        	pay_chk_green.setSelected(false);
        	pay_combo_red.setEnabled(false);
        	pay_combo_sky.setEnabled(false);
        	pay_combo_purple.setEnabled(false);
        	pay_combo_navy.setEnabled(false);
        	pay_combo_green.setEnabled(false);
    	}
    }

    private void pay_bt_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_bt_bookActionPerformed
		if (pay_lb_seat.getText().isEmpty()||pay_lb_seat.getText().equals("")) {
			showMsg("선택한 좌석이 없습니다.");
			return;
		} else {
			int con = JOptionPane.showConfirmDialog(this, "예매 하시겠습니까?");// Y:0,N:1,C:2
			if (con == 0) {
				if(seatinfo.get(0).getSeat_ox_fk2().equals("NO")) {
					int play_number = seatinfo.get(0).getPlay_number_fk2();
					int seat_number = seatinfo.get(0).getSeat_number_fk2();
					
					int n1 = playDAO.seat_ticket(play_number, seat_number,"YES",seatinfo.get(0).getPlay_number_fk2(),
							seatinfo.get(0).getplay_region_fk2(),seatinfo.get(0).getSeat_number_fk2(),
							seatinfo.get(0).getSeat_grade_fk2(),seatinfo.get(0).getSeat_price_fk2(),
							seatinfo.get(0).getPlay_schedule_fk2());
					if(n1>0) {
						seat.add(seatinfo.get(0));
						Object[][] data = new Object[seat.size()][3];
						for (int i = 0; i < seat.size(); i++) {
							data[i][0] = seat.get(i).getPlay_number_fk2();
							data[i][1] = seat.get(i).getSeat_number_fk2();
							data[i][2] = seat.get(i).getSeat_price_fk2();
						}
						
						String[] colNames = {"경기번호", "선택좌석", "결제금액" };
						seat_Table.setModel(new DefaultTableModel(data, colNames) {
							boolean[] canEdit = new boolean[] { false, false, false };

							public boolean isCellEditable(int rowIndex, int columnIndex) {
								return canEdit[columnIndex];
							}
						});
						String str=(n1>0)?"예매 완료!":"예매 실패!";
			        	showMsg(str);
					}
				}else {
					showMsg("좌석이 없습니다.");
				}
				
				seatinfo.remove(0);
				pay_lb_price.setText("");
				pay_lb_seat.setText("");
				return;
			} else {
				showMsg("취소하셨습니다.");
				return;
			}
		}
    	
    }//GEN-LAST:event_pay_bt_bookActionPerformed

