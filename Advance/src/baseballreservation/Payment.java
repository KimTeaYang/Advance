	private void pay_bt_bookcanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_bt_bookcanActionPerformed
		int con = JOptionPane.showConfirmDialog(this, "예매를 취소하시겠습니까?");// Y:0,N:1,C:2
		if(con==0) {
			for(int i=0;i<seat.size();i++) {
				playDAO.ticket_delete(seat.get(i).getPlay_number_fk2(), 
						seat.get(i).getSeat_number_fk2(),"NO");
			}
			seat.clear();
			seat_Table.setModel(new DefaultTableModel());
			showMsg("모든 예매가 취소되었습니다.");
		}else {
			showMsg("예매 취소가 완료되지 않았습니다.");
		}
    }
    

